#include<stdio.h>
int main(){
	int a = 10, b = 20;
	printf("Bitwise AND(&) operator = %d\n",a&b); //Bitwise AND
	printf("Bitwise OR(|) operator = %d\n",a|b);  //Bitwise OR
	printf("Bitwise XOR(^) operator = %d\n",a^b); //Bitwise XOR
	printf("Bitwise NOT(~) operator = %d\n",~a);  //Bitwise NOT
	printf("Bitwise LEFT SHIFT(<<) operator = %d\n",a<<1); //Bitwise Left Shift
	printf("Bitwise RIGHT SHIFT(>>) = %d\n",b>>1); //Bitwise Right Shift 
}  
