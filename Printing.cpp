#include<iostream>
using namespace std;

int main(){
	cout<<"Hello World";
	cout<<endl; //For new Line 
	cout<<"\nHello India"; // \n is also used for new line
	cout<<endl;
	//Other way for "endl"
	cout<<"Hello World"<<endl;
	cout<<"\nHello India"<<endl;
	
	cout<<"Hello World"<<endl<<"Hello India"; // All in one line;
	
}
